#include <unistd.h>

void ft_print_comb (void)
{
	char x = '0';
	char y;
	char z;
	
	while (x <= '7')
	{
		y = x + 1;
		while (y <= '8')
		{
			z = y +1;
			while (z <= '9')
			{
				write(1,&x,1);
				write(1,&y,1);
				write(1,&z,1);
				if ((x != '7') || (y !='8') || (z !='9'))
					{
						write(1,&", ",2);
					}
				z ++;
			}
			y ++;
		}
		x ++;
	}
}

int main (void)
{
	ft_print_comb();
	return(0);
}
